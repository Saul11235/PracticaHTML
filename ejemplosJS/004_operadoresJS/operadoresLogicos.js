//Analizando operadores logicos

//AND  - doble amspersand &&  --------------------------------
console.log("\nOperador logico and ----- && ")
var and1= true &&  true
var and2= false && false
var and3= true &&  false
console.log("true and true  ",and1)
console.log("false and false",and2)
console.log("true and false ",and3)
//es posible alinear varios operadores and &&
console.log(true && true && true && true && true)
//evalua todo hasta que encuentre false y lo asigna a la rpta
//no es conveniente usar objetos que nos son booleanods

//OR - doble raya ||  ----------------------------------------
console.log("\nOperador logico or ------ ||")
var or1= true || true
var or2= false ||false
var or3= true || false
console.log("true or true  ",or1)
console.log("false or false",or2)
console.log("true or false ",or3)
//es posible alinear varios operadores or ||
console.log(true || true || true || true || true)
//evalua todo hasta que encuentra true u lo asigna a la rpta
//se puede usar todo tipo de objetos

